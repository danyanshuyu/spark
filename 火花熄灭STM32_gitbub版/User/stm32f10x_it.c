/*******************************************************************************
* Function Name  : USART2_IRQHandler
* Description    : This function handles USART2 global interrupt request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void USART2_IRQHandler(void)		 
{
   u8 Data = 0;
   static u8 Count = 0;
	
	/*处理接受到的数据*/
	if(USART_GetITStatus(USART2, USART_IT_RXNE) == SET)//判断是否接受到数据
	{
		Data = USART_ReceiveData(USART2);//读取串口数据
		
		if(Data == 0xEE)//接收到数据帧头EE		   数据帧尾FFFCFFFF
		{
			ScreenData.Busy = 1;//数据处于接收状态
			ScreenData.TranFinsh = 0;//接收未完成
			ScreenData.Count = 0;//接收字节数清零
		}
		ScreenData.RcevData[ScreenData.Count++] = Data;//接收到的数据存入结构体

		//判断是否接收结束
		switch(Count)
		{
			case 0:
				if(Data == 0xFF)
				{
					Count = 1;
				}
				else
				{
					Count = 0;
				}
				break;
			case 1:
				if(Data == 0xFC)
				{
					Count = 2;
				}
				else
				{
					Count = 0;
				}
				break;
			case 2:
				if(Data == 0xFF)
				{
					Count = 3;
				}
				else
				{
					Count = 0;
				}
				break;
			case 3:
				if(Data == 0xFF)
				{
					Count = 0;
					ScreenData.Busy = 0;//数据接收完成状态
					ScreenData.TranFinsh = 1;//接收完成
				}
				else
				{
					Count = 0;
				}
				break;

			default:break;
		}
	}

	if(USART_GetITStatus(USART2,USART_IT_RXNE) != RESET)//清除接受中断标志位
	{
		USART_ClearITPendingBit(USART2,USART_IT_RXNE);
	}
}

