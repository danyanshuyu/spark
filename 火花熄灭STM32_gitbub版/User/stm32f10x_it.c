/*******************************************************************************
* Function Name  : TIM2_IRQHandler
* Description    : This function ha+ndles TIM2 global interrupt request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void TIM2_IRQHandler(void)
{
	if (TIM_GetITStatus(TIM2, TIM_IT_Update) != RESET)	//检测指定的中断是否发生
	{
		TIM_ClearITPendingBit(TIM2,TIM_IT_Update);      //清除中断处理位

		
		//火花检测程序	 //每500ms检测一次
		if(Single_Flag == 1)
		{
			Single_temp1 = FSMC_CPLD_Read(0x11);	     //读出累加器的起始值
			Single_Flag = 0;
		}
		else
		{
		    Single_temp2 = FSMC_CPLD_Read(0x11);		 //再次读出累加器的值
			Single_num = Single_temp2 -	Single_temp1;	 //相减得出一次的火花数
			Single_temp1 = Single_temp2;
			if(Single_num < 0)	
			Single_num = Single_num + 256;				 //累加器是一个字节，从0到255往复累加 ，要确保火花数为正
			if(Test_mode_Flag == 0)			
			{
				Spark_test();                    //火花检测
			}
			else
			{
				if(Single_num == 0) Test_result_Flag = 1;	//传感器故障
				if(Single_num != 0) Test_result_Flag = 2;	//传感器功能正常
			}			
		}
		
		//在有更新火花事件个数的情况下，屏幕更新标志位置1				
	    if(Alarm_num != _24C08_I2C_HalfwordRead(0))				  
		{
			Screen_update_Flag = 1;
		}
			  
	}
}

/*******************************************************************************
* Function Name  : TIM3_IRQHandler
* Description    : This function handles TIM3 global interrupt request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void TIM3_IRQHandler(void)
{
    u8 i = 0;
	if (TIM_GetITStatus(TIM3, TIM_IT_Update) != RESET)	//检测指定的中断是否发生
	{
		TIM_ClearITPendingBit(TIM3,TIM_IT_Update);      //清除中断处理位
		for(;i <= 99;i++)
		{
			if(Spray_Event[i] != 0) 
			{
				Spray_Event[i]++;
				if(Spray_Event[i] > 50)					//延迟时间 50*0.1s=5s
				{
					Spray_Event[i] = 0;
					Data_Save_End();					//记录火花事件结束时间，
					Screen_End_update();				//并显示
				}
			}
		}


		if(SprayTime_Cnt != 0) SprayTime_Cnt++;
		if(SprayTime_Cnt > 50)
		{
			FSMC_CPLD_Write(CPLD_0x840_Status&=0xFD,0x840);      //到规定时间，关闭喷水
	   		SprayTime_Cnt = 0;
		}

		
		if(Alarm_Flag== 1)				                         //声光报警持续一段时间后关闭 单位：秒
		{
		   AlarmTime_Cnt++;
		   if(AlarmTime_Cnt == 10*(Alarm_TIME))
		   {
		        FSMC_CPLD_Write(CPLD_0x840_Status&=0xFE,0x840);      //到规定时间，关闭声光报警
		   		AlarmTime_Cnt = 0;
				Alarm_Flag = 0;
		   }  
		}

		TIM3_Open_Cut++;
		if(TIM3_Open_Cut == 1000)								 //到极限时间，关闭定时器
		TIM_Cmd(TIM3,DISABLE);
	}	
}

